def car(tata): # argument function
    print(tata)

car("Audi")

def greet(): #Non Argument function
    print("Hello! Everyone")

greet()

def add(a, b): # Return Type Function
    return a + b

result = add(5, 4)
print(result)

def sub(a, b):
    return a - b

result = sub(5, 4)
print(result)

def mul(a,b):
    return a*b

result = mul(5,4)
print(result)

def div(a,b):
    return a/b
result = div(5,4)
print(result)

